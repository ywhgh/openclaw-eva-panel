const storedLang = localStorage.getItem('eva-lang') as LangName | null;
const currentLang = ref<LangName>(storedLang && messages[storedLang] ? storedLang : 'zh-en');

export function useI18n() {
  function t(key: string): string {
    return messages[currentLang.value][key] ?? key;
  }

  function setLang(lang: LangName): void {
    currentLang.value = lang;
    localStorage.setItem('eva-lang', lang);
  }

  const options = computed(() => Object.entries(langOptions).map(([value, label]) => ({
    value: value as LangName,
    label,
  })));

  const currentLangLabel = computed(() => langOptions[currentLang.value]);

  return {
    currentLang,
    currentLangLabel,
    langOptions: options,
    setLang,
    t,
  };
}
